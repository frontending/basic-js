    encrypt(message, key) {
        if (message === undefined || key === undefined) {
            throw Error('Incorrect arguments!')
        }
        let encryptResult = ''
        message = message.toUpperCase()
        key = key.toUpperCase()
        if (message.length < key.length) {
            key = key.slice(0, message.length)
        } else {
            while (message.length > key.length) {
                key += key
            }
            key = key.slice(0, message.length)
        }
        let n = 0
        for (let i = 0; i < message.length; i++) {
            if (this.letters.indexOf(message[i]) == '-1') {
                encryptResult += message[i]
                n++
            } else {
                encryptResult += this.lettersMatrix[
                    this.letters.indexOf(key[i - n])
                ][this.letters.indexOf(message[i])]
            }
        }
        if (this.direct) {
            return encryptResult
        } else {
            return encryptResult.split('').reverse().join('')
        }
    }
    decrypt(message, key) {
        if (message === undefined || key === undefined) {
            throw Error('Incorrect arguments!')
        }
        let decryptResult = ''
        message = message.toUpperCase()
        key = key.toUpperCase()
        if (message.length < key.length) {
            key = key.slice(0, message.length)
        } else {
            while (message.length > key.length) {
                key += key
            }
            key = key.slice(0, message.length)
        }
        let n = 0
        for (let i = 0; i < message.length; i++) {
            if (this.letters.indexOf(message[i]) == '-1') {
                decryptResult += message[i]
                n++
            } else {
                decryptResult += this.letters[
                    this.lettersMatrix[
                        this.letters.indexOf(key[i - n])
                    ].indexOf(message[i])
                ]
            }
        }
        if (this.direct) {
            return decryptResult
        } else {
            return decryptResult.split('').reverse().join('')
        }
    }
